    private void btnIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIActionPerformed
        String correo = txtEmailI.getText();
        String clave = txtPassI.getText();
        boolean existe = false;
        
        if(correo.equals("") || clave.equals("")){
            JOptionPane.showMessageDialog(null, "Debe de ingresar un correo y clave para ingresar");
        }else{
            for(int i=0; i<Reproductor.lista.size(); i++){
                if(Reproductor.lista.get(i).correo.equals(correo) && Reproductor.lista.get(i).clave.equals(clave)){
                    Reproductor.posicionUsuario = i;
                    existe = true;
                }
            }
        }
        
        if(existe){
            txtEmailI.setText("");
            txtPassI.setText("");
            Menu u = new Menu();
            u.setVisible(true);
            this.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(null, "Los datos ingresados son incorrectos");
        }
        
    }//GEN-LAST:event_btnIActionPerformed

    private void btnRActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRActionPerformed
        String correo = txtEmailR.getText();
        String clave = txtPassR.getText();
        String nombre = txtUserR.getText();
        
        if(correo.equals("") || clave.equals("") || nombre.equals("")){
            JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios");
        }else{
            txtEmailR.setText("");
            txtPassR.setText("");
            txtUserR.setText("");
            Reproductor.lista.add(new Usuario(nombre, correo, clave));
            JOptionPane.showMessageDialog(null, "Se ha creado correctamente el usuario");
        }
    }//GEN-LAST:event_btnRActionPerformed

    
