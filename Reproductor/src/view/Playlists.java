    private void btnSubirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubirActionPerformed
        JFileChooser fc1 = new JFileChooser();
        fc1.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        int returnVal = fc1.showOpenDialog(null);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            portada = fc1.getSelectedFile();
            Image img= new ImageIcon(portada.getAbsolutePath().replace('\'', '/')).getImage();
            ImageIcon img2=new ImageIcon(img.getScaledInstance(128, 128, Image.SCALE_SMOOTH));
            lblPortada.setIcon(img2);
        }
    }//GEN-LAST:event_btnSubirActionPerformed

    private void listaCancionesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listaCancionesMouseClicked
        int indice = listaCanciones.getSelectedIndex();
        portada = misPlaylists.get(indice).getPortada();
        String nombre = misPlaylists.get(indice).getNombre();
        String genero = misPlaylists.get(indice).getGenero();
        String descripcion = misPlaylists.get(indice).getDescripcion();
        
        txtNombre.setText(nombre);
        txtGenero.setText(genero);
        txtDescripcion.setText(descripcion);
        Image img= new ImageIcon(portada.getAbsolutePath().replace('\'', '/')).getImage();
        ImageIcon img2=new ImageIcon(img.getScaledInstance(128, 128, Image.SCALE_SMOOTH));
        lblPortada.setIcon(img2);
        seleccionado = true;
    }//GEN-LAST:event_listaCancionesMouseClicked

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int indice = listaCanciones.getSelectedIndex();
        String nombre = txtNombre.getText();
        String genero = txtGenero.getText();
        String descripcion = txtDescripcion.getText();
        
        if(seleccionado){
            defaultModel.set(indice, nombre);
            misPlaylists.get(indice).setNombre(nombre);
            misPlaylists.get(indice).setGenero(genero);
            misPlaylists.get(indice).setDescripcion(descripcion);
            misPlaylists.get(indice).setPortada(portada);
            
            Reproductor.playlists.get(misPlaylists.get(indice).getId()).setNombre(nombre);
            Reproductor.playlists.get(misPlaylists.get(indice).getId()).setGenero(genero);
            Reproductor.playlists.get(misPlaylists.get(indice).getId()).setDescripcion(descripcion);
            Reproductor.playlists.get(misPlaylists.get(indice).getId()).setPortada(portada);
            
            File f = new File(".");
            portada = new File(f.getAbsolutePath() + "\\cerrar.png");
            txtNombre.setText("");
            txtGenero.setText("");
            txtDescripcion.setText("");
            Image img= new ImageIcon(portada.getAbsolutePath().replace('\'', '/')).getImage();
            ImageIcon img2=new ImageIcon(img.getScaledInstance(128, 128, Image.SCALE_SMOOTH));
            lblPortada.setIcon(img2);
            seleccionado = false;
            JOptionPane.showMessageDialog(null, "La playlist se ha modificado correctamente");
        }else{
            JOptionPane.showMessageDialog(null, "No se ha seleccionado ninguna playlist");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEscucharActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEscucharActionPerformed
        int indice = listaCanciones.getSelectedIndex();
        if(seleccionado){
            UserInterface u = new UserInterface(misPlaylists.get(indice).getCanciones());
            u.setVisible(true);
            this.setVisible(false);
        }else{
            JOptionPane.showMessageDialog(null, "No se ha seleccionado ninguna playlist");
        }
    }//GEN-LAST:event_btnEscucharActionPerformed

    
